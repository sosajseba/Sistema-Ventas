    void mostrarLista(){

        List<Vendedor> lista = vCon.listar();
        modeloTabla = (DefaultTableModel) tabla.getModel();
        Object[] obj = new Object[5];
        for (int i = 2; i < lista.size(); i++) {

            obj[0] = lista.get(i).getIdvendedor();
            obj[1] = lista.get(i).getNombres();
            obj[2] = lista.get(i).getDni();
            obj[3] = lista.get(i).getTelefono();
            obj[4] = lista.get(i).getUser();
            modeloTabla.addRow(obj);

        }

        tabla.setModel(modeloTabla);

    }
    
    void listarBusqueda() {

        if (nombreVendedor.getText().equals("")) {

            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre!");

        } else {

            limpiarTabla();
            List<Vendedor> lista = vCon.listarBusqueda(nombreVendedor.getText());
            modeloTabla = (DefaultTableModel) tabla.getModel();
            Object[] obj = new Object[5];
            for (int i = 0; i < lista.size(); i++) {

                obj[0] = lista.get(i).getIdvendedor();
                obj[1] = lista.get(i).getNombres();
                obj[2] = lista.get(i).getDni();
                obj[3] = lista.get(i).getTelefono();
                obj[4] = lista.get(i).getUser();
                modeloTabla.addRow(obj);

            }

            tabla.setModel(modeloTabla);
            
            if (lista.isEmpty()) {
                
                JOptionPane.showMessageDialog(this, "El vendedor no existe!");
                
                mostrarLista();
                
            }

        }
        
    }

